#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}

/*										*/
/*	La función lstiter recorre la lista. Mientras se recorre la lista,	*/
/*	pasamos la función pasada como parámetro.				*/
