#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}

/*										*/
/*	La funcion putendl escribe una string mediante la función write con	*/
/*	la salida fd. Finalmente escribe una nueva línea.			*/
/*										*/
