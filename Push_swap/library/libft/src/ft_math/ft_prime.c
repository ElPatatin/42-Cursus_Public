#include "../../inc/libft.h"

int
	ft_prime(int nb)
{
	int	i;
	int	n;

	i = 2;
	n = 1;
	if (nb <= 1)
		n = 0;
	while (i <= (nb / 2))
	{
		if ((nb % i) == 0)
		{
			n = 0;
			break ;
		}
		i++;
	}
	return (n);
}
