#include "../../inc/libft.h"

int	ft_nbrlen(t_ll nbr)
{
	int	len;

	len = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
		len++;
	while (nbr)
	{
			nbr /= 10;
			len++;
	}
	return (len);
}
