#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}

/*										*/
/*	La función tolower transforma un carácter ascii dentro del alfabeto	*/
/*	de mayuscúlas al alfabeto de minúsculas.			*/
